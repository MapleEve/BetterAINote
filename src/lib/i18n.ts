function resolvePath(tree: TranslationTree, path: string): string | null {
    const value = path
        .split(".")
        .reduce<string | TranslationTree | undefined>(
            (current, key) =>
                typeof current === "object" && current !== null
                    ? current[key]
                    : undefined,
            tree,
        );

    return typeof value === "string" ? value : null;
}

export function translate(
    language: UiLanguage,
    key: string,
    replacements?: Record<string, string | number>,
) {
    const value =
        resolvePath(translations[language], key) ??
        resolvePath(translations.en, key) ??
        key;

    if (!replacements) {
        return value;
    }

    return Object.entries(replacements).reduce(
        (message, [name, replacement]) =>
            message.replaceAll(`{${name}}`, String(replacement)),
        value,
    );
}
